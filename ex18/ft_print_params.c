#include <unistd.h>

void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int argc, char *argv[])
{
	int	a;

	a = 1;
	while (a != argc)
	{
		ft_putstr(argv[a]);
		ft_putstr("\n");
		a++;
	}
	return (0);
}
